"""
题目描述
棋盘上 A 点有一个过河卒，需要走到目标 B 点。卒行走的规则：可以向下、或者向右。
同时在棋盘上 C 点有一个对方的马，该马所在的点和所有跳跃一步可达的点称为对方马的控制点。因此称之为“马拦过河卒”。
棋盘用坐标表示，A 点 (0,0)、B 点 (n,m)，同样马的位置坐标是需要给出的。
现在要求你计算出卒从 A 点能够到达 B 点的路径的条数，假设马的位置是固定不动的，并不是卒走一步马走一步。

输入格式
一行四个正整数，分别表示 B 点坐标和马的坐标。

输出格式
一个整数，表示所有的路径条数。

输入
6 6 3 3

输出
6
"""

# 动态规划问题(dp)

xb, yb, xm, ym = map(int, input().split())


lst = set()
lst.add((xm, ym))

# 马步偏移量（固定）
moves = [(-2, 1), (-1, 2), (1, 2), (2, 1),
         (-1, -2), (-2, -1), (1, -2), (2, -1)]

for dx, dy in moves:
    nx, ny = xm + dx, ym + dy
    if 0 <= nx <= xb and 0 <= ny <= yb:
        lst.add((nx, ny))

dp = [[0] * (yb + 1) for _ in range(xb + 1)]  # 生成一个（xb，yb）的二维列表

dp[0][0] = 1

for i in range(xb + 1):        # i 是 x 坐标（行），从 0 到 6
    for j in range(yb + 1):    # j 是 y 坐标（列），从 0 到 6
        if (i, j) in lst:    # 如果这个点是马的控制点
            continue              # 跳过，不计算（dp[i][j] 保持 0）
        if i > 0:                 # 如果不是第一行（可以从上面下来）
            dp[i][j] += dp[i-1][j]   # 加上从上方格子来的路径数
        if j > 0:                 # 如果不是第一列（可以从左边过来）
            dp[i][j] += dp[i][j-1]   # 加上从左方格子来的路径数

print(dp[xb][yb])
